	private void processDataSet(DataSet dataSet, DefaultMutableTreeNode node, String featureVanueName){
        if(dataSet.toString() != null) System.out.println(dataSet);
        if(dataSet.getEntropy() != 0){
            System.out.println("Thuộc tính tốt nhất là " + dataSet.getSplitOnFeatute() + " " + dataSet.getSplitOnFeatute().getFeatureValues());
            HashMap<String, DataSet> featureDataSets = new HashMap<String, DataSet>();
            dataSet.getSplitOnFeatute().getFeatureValues().forEach(featureValue -> 
                featureDataSets.put(featureValue.getName(), dataSet.createDataset(dataSet.getSplitOnFeatute(), featureValue, dataSet.getData())));
            processDataSets(featureDataSets, node);
        }else{
            String[][] data = dataSet.getData();
            String decision = " [" + data[0][data[0].length - 1] + " = " + data[1][data[0].length - 1] + "]";
            node.add(new DefaultMutableTreeNode(featureVanueName +" : " + decision));
            System.out.println("Decision ==> " + decision);
        }
    }

    private void processDataSets(HashMap<String, DataSet> dataSets, DefaultMutableTreeNode node) {
        dataSets.keySet().forEach(dataSet -> {
            if(dataSets.get(dataSet).getEntropy() != 0){
                   DefaultMutableTreeNode newNode = new DefaultMutableTreeNode(dataSet + "   : [" + dataSets.get(dataSet).getSplitOnFeatute().getName()+"]");
                   node.add(newNode);
                   processDataSet(dataSets.get(dataSet), newNode, dataSet);
            }
            else processDataSet(dataSets.get(dataSet), node, dataSet);
        });
    }
